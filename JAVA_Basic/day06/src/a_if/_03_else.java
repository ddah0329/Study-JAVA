package a_if;

public class _03_else {
	public static void main(String[] args) {

		// else는 if와 함께 사용해야 한다.
		// else는 위의 조건식이 모두 거짓이면 실행한다.
		
		// 만약에 내 조건이 참이면 실행하고, 아니면 else 블록을 실행한다. 
		if(false) {
			System.out.println("if 영역");
		} else { // if가 fasle이므로 true인 else를 실행한다. 
			System.out.println("else 영역");
			
		// result
		// else 영역
		}
	}
}
